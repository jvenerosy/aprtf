<section class="section">
    <div class="container is-max-small">
        <h2 class="title is-2 has-text-centered">Qu’est-ce que la thérapie multifamiliale ?</h2>
        <p class="text marginless">La thérapie multifamiliale (TMF) ou aussi travail multifamilial, est une modalité thérapeutique qui regroupe plusieurs familles dans le même dispositif et dans le même temps. Ces familles partagent toutes une problématique commune qui est la cible de la TMF.</p>
    </div>
</section>
<section class="section has-background">
    <div class="container is-max-small">
        <h2 class="title is-2 has-text-centered">Dans quels contextes se pratique-t-elle ?</h2>
        <p class="text">La TMF se pratique dans les milieux hospitaliers (intra et ambulatoires), dans les structures médico-sociales, dans les structures sociales et dans les écoles. Elle est complémentaire des autres soins et/ou accompagnements qui sont mis en œuvre dans ces structures. Elle peut même parfois se dérouler dans des lieux de la vie de tous les jours (centres commerciaux, parcs, transports en communs…).</p>
        <p class="text highlighted">Elle s’applique à des problématiques très diverses (de santé mentale ou physique, mais aussi relationnelles), toujours avec un cadre souple et ajusté au plus près des besoins des familles et des institutions.</p>
    </div>
</section>
<section class="section">
    <div class="container is-max-small">
        <h2 class="title is-2 has-text-centered">Quels sont ses principes ?</h2>
        <p class="text">La TMF s’appuie sur des concepts issus de l’approche systémique, de la thérapie basée sur la mentalisation, de la thérapie de groupe, des approches cognitivo-comportementales, des médiations artistiques et de la psychoéducation.</p>
        <p class="text">Elle vise à créer un contexte propice à l’entraide entre les familles, à l’émergence de savoirs implicites et de nouvelles compétences, à la mise en perspective des problématiques singulières grâce à l’effet de miroir. Elle permet de lutter contre l’isolement social des familles, souvent induit par leurs problèmes. Enfin, elle permet de renverser les rapports entre professionnels et familles, le rôle plus périphérique et catalyseur des premiers, et leur faible nombre, autorisant des relations plus collaboratives et horizontales.</p>
    </div>
</section>
<section class="section has-background">
    <div class="container is-max-small">
        <h2 class="title is-2 has-text-centered">Comment se déroule-t-elle ?</h2>
        <p class="text">Les TMF regroupent entre 5 et 10 familles qui partagent le même problème ou dont un de leur membre est dans le même dispositif (école, hospitalisation, service de placement…). Les familles sont accompagnées d’un faible nombre de professionnels qui conduisent les séances, en général entre 2 et 4, Le format de la TMF est d’une grande variabilité selon les programmes conçus par les équipes : dans la durée du programme (6 à 12 mois), dans la durée des séances (2 à 7 heures), dans l’espacement des séances (quotidiennes, hebdomadaires, mensuelles, etc…), dans le format du groupe (ouvert, semi-ouvert, fermé).</p>
        <p class="text">Dans les séances, les familles abordent leurs problèmes sous la forme d’ateliers, le plus souvent ludiques, qui génèrent une expérience propice à la réflexion et au recadrage des problèmes pour amener à des changements concrets dans la vie des gens, soutenus par l’ensemble du groupe. Des familles « expertes », qui ont déjà fait le programme, sont souvent incluses comme facilitatrices de l’adhésion des familles.</p>
        <p class="text">La TMF se complète d’interventions familiales singulières, pendant les séances (interventions brèves) ou en parallèle du programme de TMF. Elles permettent la personnalisation des objectifs et le soutien aux actions mises en œuvre par les familles.</p>
    </div>
</section>
<section class="section">
    <div class="container is-max-small">
        <h2 class="title is-2 has-text-centered">Quels sont ses résultats ?</h2>
        <p class="text">La TMF a fait l’objet d’évaluations qualitatives et quantitatives, dont les résultats ont été rapportés par plusieurs revues de la littérature et méta-analyses récentes. Ces travaux montrent des effets positifs sur la symptomatologie ou les problèmes, mais aussi sur la santé mentale des proches et sur les compétences relationnelles à l’intérieur et à l’extérieur de la famille. En outre, la TMF se caractérise par un faible taux d’abandon de la thérapie.</p>
    </div>
</section>
<section class="section has-background">
    <div class="container is-max-small">
        <h2 class="title is-2 has-text-centered">Où est-elle développée ?</h2>
        <div class="columns is-multiline">
            <div class="column is-full">
                <p class="text marginless">La TMF connait plusieurs courants (psychoéducatif, psychanalytique et systémique), nés sur divers continents. La TMF Systémique est développée à la fin des années 1970 en Grande-Bretagne dans une institution de jour pour enfants « multi-problématiques » par Alan Cooklin et Eia Asen. Rapidement l’institution devient multifamiliale et devant le succès de cette approche, la pratique s’étend à de nombreux contextes cliniques (anorexie mentale, troubles du comportement, schizophrénie, asthme…) et sociaux (protection de l’enfance, difficultés d’inclusion scolaire, divorce…). Depuis le début des années 2000, elle est largement diffusée et pratiquée en Allemagne, aux Pays-Bas, dans les pays scandinaves, en Suisse, en Italie, en Espagne.</p>
            </div>
            <div class="column">
                <p class="text marginless">En France, elle est développée par Solange Cook-Darzens à Paris, qui a écrit de nombreux ouvrages collectifs sur le sujet. Plus récemment, le Pr. Eia Asen a implanté la formation à la TMF en 2019, qui a permis de former plus de 30 équipes et pas moins de 90 professionnels. Elle se diffuse rapidement dans l’espace francophone européen (Suisse, Belgique), en région parisienne et en province. De nombreuses équipes de psychiatrie de l’enfant, de l’adolescent et de l’adulte, d’institutions médico-sociales, et scolaires, la pratiquent.</p>
            </div>
            <div class="column">
                <p class="text marginless">Les différentes équipes formées depuis 2019 continuent à entretenir des liens de soutien et de partage autour de leur pratique, et dans le cadre de la mise en place d’un comité européen des TMF, le groupe ressource francophone des TMF devrait voir le jour fin 2024, pour porter notamment l’organisation d’un congrès européen en 2027. Les équipes francophones ont participé au congrès européen de TMF à Potsdam en 2023 et seront également représentées à celui qui se tiendra à Visselhövede (Allemagne) les 4, 5 et 6 septembre 2025.</p>
            </div>
        </div>
    </div>
</section>
